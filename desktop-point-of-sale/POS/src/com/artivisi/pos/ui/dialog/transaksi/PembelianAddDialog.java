    private void calculateSubTotal(){
        if(txtHargaBeli.getText().length()>0 && txtQuantity.getText().length()>0){
            BigDecimal hargaBeli = TextComponentUtils.parseNumberToBigDecimal(txtHargaBeli.getText());
            Integer qty = Integer.valueOf(txtQuantity.getText());
            BigDecimal subtotal = hargaBeli.multiply(new BigDecimal(qty));
            txtSubTotal.setText(TextComponentUtils.formatNumber(subtotal));
        }
    }


