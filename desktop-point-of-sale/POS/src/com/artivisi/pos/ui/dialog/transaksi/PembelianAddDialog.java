    public PembelianDetail showDialog(PembelianDetail pembelianDetail){
        this.pembelianDetail = pembelianDetail;
        loadDomainToForm();
        setVisible(true);
        return this.pembelianDetail;
    }
    private void loadDomainToForm(){
        selectedProduk = pembelianDetail.getProduk();
        txtProduk.setText(selectedProduk.getNama());
        txtQuantity.setText(pembelianDetail.getKuantitas().toString());
        txtHargaBeli.setText(
            TextComponentUtils.formatNumber(pembelianDetail.getHarga()));
        txtSubTotal.setText(
            TextComponentUtils.formatNumber(pembelianDetail.getSubTotal()));
    }
    private void loadFormToDomain(){
        pembelianDetail.setProduk(selectedProduk);
        pembelianDetail.setHarga(
            TextComponentUtils.parseNumberToBigDecimal(txtHargaBeli.getText()));
        pembelianDetail.setSubTotal(
            TextComponentUtils.parseNumberToBigDecimal(txtSubTotal.getText()));
        pembelianDetail.setKuantitas(Integer.valueOf(txtQuantity.getText()));
    }

    private boolean validateForm(){
        if(txtProduk.getText().length() == 0 || selectedProduk==null){
            JOptionPane.showMessageDialog(FrameUtama.getInstance(),
                    "Pilih produk terlebih dahulu!","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(txtHargaBeli.getText().length()==0){
            JOptionPane.showMessageDialog(FrameUtama.getInstance(),
                    "Harga beli harus diisi!","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(txtQuantity.getText().length()==0 || 
                Integer.valueOf(txtQuantity.getText()) <= 0){
            JOptionPane.showMessageDialog(FrameUtama.getInstance(),
                    "Quantity harus diisi dan > 0!","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

