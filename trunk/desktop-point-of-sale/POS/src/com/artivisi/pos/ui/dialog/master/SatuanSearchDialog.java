    private void txtCariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyReleased
        for(int i=0;i<tblSatuan.getRowCount();i++){
            String current = tblSatuan.getValueAt(i, 0).toString();
            if(current.toLowerCase().indexOf(txtCari.getText().toLowerCase())>=0){
                tblSatuan.getSelectionModel().setSelectionInterval(i, i);
                tblSatuan.getColumnModel().getSelectionModel().setSelectionInterval(0, 0);
                TextComponentUtils.scrollToRect(tblSatuan, i);
                break;
            }
        }
}//GEN-LAST:event_txtCariKeyReleased

