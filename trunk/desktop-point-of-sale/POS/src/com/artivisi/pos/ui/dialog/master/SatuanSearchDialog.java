    public Satuan showDialog(){
        Satuans = FrameUtama.getMasterService().semuaSatuan();
        tblSatuan.setModel(new SatuanTableModel());
        setVisible(true);
        if(isOk == false) selectedSatuan = null;
        return selectedSatuan;
    }

    private class SatuanTableModel extends AbstractTableModel{

        public int getRowCount() {
            return Satuans.size();
        }

        public int getColumnCount() {
            return 2;
        }

        public Object getValueAt(int rowIndex, int columnIndex) {
            Satuan p = Satuans.get(rowIndex);
            switch(columnIndex){
                case 0 : return p.getNama();
                case 1 : return p.getId();
                default: return "";
            }
        }

    }

