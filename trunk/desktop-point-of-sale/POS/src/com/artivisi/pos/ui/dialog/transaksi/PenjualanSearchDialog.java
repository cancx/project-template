    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        for(int i=0;i<tblPenjualan.getRowCount();i++){
            String current = tblPenjualan.getValueAt(i, 0).toString();
            if(current.toLowerCase().indexOf(txtSearch.getText().toLowerCase())>=0){
                tblPenjualan.getSelectionModel().setSelectionInterval(i, i);
                tblPenjualan.getColumnModel().getSelectionModel().setSelectionInterval(0, 0);
                TextComponentUtils.scrollToRect(tblPenjualan, i);
                break;
            }
        }
}//GEN-LAST:event_txtSearchKeyReleased

