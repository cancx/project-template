     public Penjualan showDialog(){
        penjualans = FrameUtama.getTransaksiService().semuaPenjualan();
        tblPenjualan.setModel(new PenjualanTableModel());
        setVisible(true);
        if(isOk == false) selectedPenjualan = null;
        return selectedPenjualan;
    }
    private class PenjualanTableModel extends AbstractTableModel{
        public int getRowCount() {
            return penjualans.size();
        }
        public int getColumnCount() {
            return 3;
        }
        public Object getValueAt(int rowIndex, int columnIndex) {
            Penjualan p = penjualans.get(rowIndex);
            switch(columnIndex){
                case 0 : return p.getId();
                case 1 : return p.getTanggal();
                case 2 : return p.getTotal();
                default: return "";
            }
        }
    }
    private class PenjualanSelectionListener implements ListSelectionListener{
        public void valueChanged(ListSelectionEvent e) {
            if(tblPenjualan.getSelectedRow()>=0){
                int index = tblPenjualan.convertRowIndexToModel(tblPenjualan.getSelectedRow());
                selectedPenjualan = penjualans.get(index);
            }
        }
    }
