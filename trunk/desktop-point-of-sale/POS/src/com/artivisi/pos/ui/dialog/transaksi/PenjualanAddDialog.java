    private void btnLookupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLookupActionPerformed
        Produk p = new ProdukSearchDialog().showDialog();
        if(p!=null){
            selectedProduk = p;
            txtProduk.setText(selectedProduk.getNama());
            txtHargaJual.setText(TextComponentUtils.formatNumber(selectedProduk.getHargaJual()));
            if(txtQuantity.getText().length()>0){
                BigDecimal subTotal  = TextComponentUtils.parseNumberToBigDecimal(txtHargaJual.getText())
                        .multiply(new BigDecimal(txtQuantity.getText()));
                txtSubTotal.setText(TextComponentUtils.formatNumber(subTotal));
            }
        }        
    }//GEN-LAST:event_btnLookupActionPerformed

    private void txtQuantityKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtQuantityKeyReleased
        if(txtHargaJual.getText().length()>0){
            BigDecimal subTotal  = TextComponentUtils.parseNumberToBigDecimal(txtHargaJual.getText())
                    .multiply(new BigDecimal(txtQuantity.getText()));
            txtSubTotal.setText(TextComponentUtils.formatNumber(subTotal));
        }
    }//GEN-LAST:event_txtQuantityKeyReleased

