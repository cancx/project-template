    public PenjualanDetail showDialog(PenjualanDetail penjualanDetail){
        this.penjualanDetail = penjualanDetail;
        loadDomainToForm();
        setVisible(true);
        return this.penjualanDetail;
    }
    private void loadDomainToForm(){
        selectedProduk = penjualanDetail.getProduk();
        txtProduk.setText(selectedProduk.getNama());
        txtQuantity.setText(penjualanDetail.getKuantitas().toString());
        txtHargaJual.setText(
            TextComponentUtils.formatNumber(penjualanDetail.getHarga()));
        txtSubTotal.setText(
            TextComponentUtils.formatNumber(penjualanDetail.getSubTotal()));
    }
    private void loadFormToDomain(){
        penjualanDetail.setProduk(selectedProduk);
        penjualanDetail.setHarga(
            TextComponentUtils.parseNumberToBigDecimal(txtHargaJual.getText()));
        penjualanDetail.setSubTotal(
            TextComponentUtils.parseNumberToBigDecimal(txtSubTotal.getText()));
        penjualanDetail.setKuantitas(Integer.valueOf(txtQuantity.getText()));
    }

    private boolean validateForm(){
        if(txtProduk.getText().length() == 0 || selectedProduk==null){
            JOptionPane.showMessageDialog(FrameUtama.getInstance(),
                    "Pilih produk terlebih dahulu!","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(txtHargaJual.getText().length()==0){
            JOptionPane.showMessageDialog(FrameUtama.getInstance(),
                    "Harga beli harus diisi!","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(txtQuantity.getText().length()==0 ||
                Integer.valueOf(txtQuantity.getText()) <= 0){
            JOptionPane.showMessageDialog(FrameUtama.getInstance(),
                    "Quantity harus diisi dan > 0!","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
